package com.thetubeteam.mytube.modals;

public class Video {

    private String id, name, desc, thumbnail, link;

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getDesc() {
        return desc;
    }

    public String getThumbnail() {
        return thumbnail;
    }

    public String getLink() {
        return link;
    }

}
